TEST(k2node_problematic_input_tests, test1_more_specific_1) {

  struct k2node *root_node = create_k2node();

  struct k2qstate st;
  TREE_DEPTH_T treedepth = 33;
  TREE_DEPTH_T cutdepth = 3;
  init_k2qstate(&st, treedepth, 256, cutdepth);

  unsigned long col = 710230858;
  unsigned long row = 4951110;

  int already_exists;
  k2node_insert_point(root_node, col, row, &st, &already_exists);

  ASSERT_EQ(debug_validate_k2node_rec(root_node, &st, 0), 0);

  vp_t checks = {
      {col + 1, row}, {col, row + 1},     {col, row - 1},
      {col - 1, row}, {col - 1, row - 1}, {col + 1, row + 1},
  };

  int point_exists;
  k2node_has_point(root_node, col, row, &st, &point_exists);
  ASSERT_TRUE(point_exists);

  int count = 0;
  for (const auto &check : checks) {
    k2node_has_point(root_node, check.first, check.second, &st, &point_exists);
    ASSERT_FALSE(point_exists) << "failed at i = " << count << " for point ("
                               << check.first << ", " << check.second << ")";
  }

  struct mutable_pair_t {
    unsigned long col;
    unsigned long row;
  };

  mutable_pair_t scan_result;
  k2node_scan_points_interactively(
      root_node, &st,
      [](unsigned long col, unsigned long row, void *rs) {
        auto &mp = *reinterpret_cast<mutable_pair_t *>(rs);
        mp.col = col;
        mp.row = row;
      },
      &scan_result);

  ASSERT_EQ(scan_result.col, col);
  ASSERT_EQ(scan_result.row, row);

  free_rec_k2node(root_node, 0, st.cut_depth);
  clean_k2qstate(&st);
}